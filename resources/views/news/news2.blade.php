        expressivas em nível de mestrado e doutorado, conquistando prêmios concedidos pela Sociedade Brasileira de
        Computação (SBC). Além disso, destacou-se pela inserção nas decisões políticas da comunidade de pesquisa em
        Informática na Educação no Brasil, tendo presidido a CEIE nos anos de 2006 e 2007.
    </p>

    <p>
        Em 2015, foi homenageado com o Prêmio Mérito Científico em Informática na Educação e agradeceu ao público com
        espontâneas palavras de reflexão, destacando a importância da democracia que a comunidade conquistou. Esse
        prêmio foi dado em reconhecimento à contribuição para o avanço da área no Brasil. Entretanto, ainda além da
        competência técnica, Alexandre era alguém digno do título de ser humano, sendo inesquecível pelo respeito e
        afeto com que tratava as pessoas.
    </p>

    <p>
